            Popular stouts have included the so-called dry Irish stouts, notably Guinness.<br>
            Historically, the term stout was used in reference to strongly alcoholic beers. 
            In the 18th century, for example, the term stout porter was used to describe a porter, or dark beer, 
            with alcohol content above 7 percent. In some cases stout was used simply as another name for dark beer.
            Today the distinction between stout and porter remains unclear. Some brewers may distinguish stout from porter on the basis of dryness, 
            in which the stout is made with unmalted roasted barley (as opposed to malted roasted barley for a porter), or sweetness, 
            in which the stout is made with lactose (as in milk stout). However, some stout recipes incorporate malted roasts. 
            Moreover, variations on these themes, such as the addition of oats during brewing (oatmeal stout) or the use of very dark malt 
            (chocolate stout), which impart characteristics such as smoothness or flavours and aromas of chocolate, may also be found in porter recipes.
        
